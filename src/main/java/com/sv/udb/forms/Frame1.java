    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            
            boolean hasErrors = false;//Determina si se ingreso un valor invalido
            boolean valueSaved = false;//Determina si se logrÃ³ guardar un valor
            hideErrors();
            
            for (String[] component: componentNameList){
                JTextField txt = (JTextField) map.getComponentByName("txt" + component[0]);
                Component err = map.getComponentByName("err" + component[0]);
                double denomination = Double.parseDouble(component[1]);
                
                if (!txt.getText().equals("")) {
                    try {
                        if (Integer.parseInt(txt.getText()) > 0) {
                            moneyList.add(denomination, Integer.parseInt(txt.getText()));
                            txt.setText("");
                            valueSaved = true;
                        }
                        else {
                            throw new Exception ();
                        }
                    }
                    catch (Exception e) {
                        err.setVisible(true);
                        hasErrors = true;
                    }
                }
            }
            
            if (hasErrors) {
                JOptionPane.showMessageDialog(this, "Algunos valores ingresados no son vÃ¡lidos.\nLos valores vÃ¡lidos fueron ingresados.");
            }
            
            else if (valueSaved) {
                JOptionPane.showMessageDialog(this, "Valores ingresados con Ã©xito.");
            }
            
            else {
                JOptionPane.showMessageDialog(this, "Por favor ingrese un valor.");
            }
            
                
        }
        catch (NumberFormatException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, "OcurriÃ³ un error:\n" + e.getMessage());
            System.err.println(e);
        }
    }//GEN-LAST:event_btnAddActionPerformed

